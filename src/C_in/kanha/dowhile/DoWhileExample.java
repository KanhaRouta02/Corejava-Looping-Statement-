package C_in.kanha.dowhile;

public class DoWhileExample {

	
	    public static void main(String args[])
	    {
	       int a = 1;
	       while(a >= 5) {
	    	   System.out.println("hii while....");
	    	   a++;
	       }
	       
	       System.out.println("**********************");
	       
	       int b = 1;
	       do {
	    	   System.out.println("hello do while...");
	    	   b++;
	       }while(b >= 5);
	    	   
	         
	    }
	



}
